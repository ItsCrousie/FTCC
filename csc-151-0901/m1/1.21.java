public class AsciiArt {
   public static void main(String[] args) {

      // Draw tree
      System.out.println("   *");
      System.out.println("  ***");
      System.out.println(" *****");
      System.out.println("*******");
      System.out.println("  ***");

      // Draw two spaces
      System.out.println("\n");

      // Draw cat
      System.out.println("/\\   /\\");
      System.out.println("  o o");
      System.out.println(" =   =");
      System.out.println("  ---");
   }
}